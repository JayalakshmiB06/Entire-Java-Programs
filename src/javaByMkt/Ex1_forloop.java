package javaByMkt;

public class Ex1_forloop 
{

	public static void main(String[] args) 
	
{
	System.out.println("print 1 to 10 numbers");
	
	for(int i=1;i<=10;i++)
	{
		System.out.println(i);
	}
	
	System.out.println("print 19 to 26 numbers");
	
	for( int i=19;i<=26;i++)
	{
		System.out.println(i);
	}
	
	System.out.println("print 100 to 0 numbers");
	
	for( int i=100;i>=0;i--)
	{
		System.out.print(i + " ");
	}
}

}
